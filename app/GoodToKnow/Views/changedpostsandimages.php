        <p>There is a law named <b>47 U.S.C. § 230</b> or <b>Section 230</b>. Congress recognized that for user speech
            to thrive on the Internet, it had to protect the services that power users’ speech. That law states:
            "No provider or user of an interactive computer service shall be treated as the publisher or speaker of any
            information provided by another information content provider." (47 U.S.C. § 230(c)(1)).</p>
        <p>Section 230 embodies that principle that we should all be responsible for our own actions and statements
            online, but generally not those of others. The law prevents most civil suits against users or services that
            are based on what others say. When harmful speech takes place, it’s the speaker that should be held
            responsible, not the service that hosts the speech.</p>
        <p>Section 230’s protections are not absolute. It does not protect companies that violate federal criminal law.
            It does not protect companies that create illegal or harmful content. Nor does Section 230 protect companies
            from intellectual property claims.</p>
        <p>Section 230 allows for web operators, large and small, to moderate user speech and content as they see fit.
            This reinforces the First Amendment’s protections for publishers to decide what content they will
            distribute. Different approaches to moderating users’ speech allows users to find the places online that
            they like, and avoid places they don’t.</p>
